      if (track && dots.length) {
        let index = 0;
        const slideCount = dots.length;
        const setActive = (i) => {
          index = (i + slideCount) % slideCount;
          track.style.transform = `translateX(-${index * 100}%)`;
          dots.forEach((d, di) => {
            d.classList.toggle("bg-emerald-600", di === index);
            d.classList.toggle("bg-slate-300", di !== index);
            d.classList.toggle("w-6", di === index);
            d.classList.toggle("w-2.5", di !== index);
          });
        };
        const goNext = () => setActive(index + 1);
        prev?.addEventListener("click", () => setActive(index - 1));
        next?.addEventListener("click", goNext);
        dots.forEach((d) => d.addEventListener("click", () => setActive(Number(d.dataset.slide || 0))));
        let timer = setInterval(goNext, 4500);
        track.parentElement?.addEventListener("mouseenter", () => clearInterval(timer));
        track.parentElement?.addEventListener("mouseleave", () => (timer = setInterval(goNext, 4500)));
        setActive(0);
      }

      // Footer year
      const year = document.getElementById("year");
      if (year) year.textContent = String(new Date().getFullYear());

      // Copy link button
      const copyBtn = document.getElementById("copyLinkBtn");
      if (copyBtn) {
